"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
Loop infinito -> Quando um código não tem fim
"""
contador = 0

while contador <= 10:
    contador = contador + 1
    print(contador)

print('Acabou')

# Esse código basicamente começa do 0 e somar +1 até chegar em 10.
# O resultado vai de 1 a 11.